const KnowledgeArticle = () => {
  const { articleId } = useParams<{ articleId: string }>();
  const article = articleId ? allArticles[articleId] : null;

  if (!article) {
    return (
      <ServicePageLayout>
        <div className="container mx-auto px-4 py-32 text-center">
          <h1 className="text-3xl font-bold text-white mb-4">Статья не найдена</h1>
          <p className="text-white/60 mb-8">Запрашиваемая статья не существует</p>
          <Link to="/baza-znaniy">
            <Button>Вернуться в базу знаний</Button>
          </Link>
        </div>
      </ServicePageLayout>
    );
  }

  // Convert markdown-like content to formatted React elements
  const formatContent = (content: string) => {
    const lines = content.split('\n');
    const elements: JSX.Element[] = [];
    let inList = false;
    let listItems: JSX.Element[] = [];
    let listType: 'ul' | 'ol' = 'ul';
    
    const flushList = () => {
      if (listItems.length > 0) {
        if (listType === 'ul') {
          elements.push(
            <ul key={`list-${elements.length}`} className="space-y-2 my-6 ml-6">
              {listItems}
            </ul>
          );
        } else {
          elements.push(
            <ol key={`list-${elements.length}`} className="space-y-2 my-6 ml-6 list-decimal list-outside">
              {listItems}
            </ol>
          );
        }
        listItems = [];
        inList = false;
      }
    };

    lines.forEach((line, i) => {
      // Headers
      if (line.startsWith('## ')) {
        flushList();
        elements.push(
          <h2 key={i} className="text-2xl sm:text-3xl font-bold text-white mt-12 mb-6 pb-3 border-b border-white/10">
            {line.replace('## ', '')}
          </h2>
        );
        return;
      }
      if (line.startsWith('### ')) {
        flushList();
        elements.push(
          <h3 key={i} className="text-xl font-semibold text-white mt-10 mb-4">
            {line.replace('### ', '')}
          </h3>
        );
        return;
      }
      
      // Unordered lists
      if (line.startsWith('- ')) {
        if (!inList || listType !== 'ul') {
          flushList();
          listType = 'ul';
        }
        inList = true;
        const content = line.replace('- ', '');
        const formattedContent = content.replace(/\*\*([^*]+)\*\*/g, '<strong class="text-white font-semibold">$1</strong>');
        listItems.push(
          <li key={`li-${i}`} className="flex items-start gap-3 text-white/70 leading-relaxed">
            <span className="w-1.5 h-1.5 rounded-full bg-[hsl(268,83%,55%)] mt-2.5 flex-shrink-0" />
            <span dangerouslySetInnerHTML={{ __html: formattedContent }} />
          </li>
        );
        return;
      }
      
      // Ordered lists
      if (line.match(/^\d+\. /)) {
        if (!inList || listType !== 'ol') {
          flushList();
          listType = 'ol';
        }
        inList = true;
        const content = line.replace(/^\d+\. /, '');
        const formattedContent = content.replace(/\*\*([^*]+)\*\*/g, '<strong class="text-white font-semibold">$1</strong>');
        listItems.push(
          <li key={`li-${i}`} className="text-white/70 leading-relaxed pl-1" dangerouslySetInnerHTML={{ __html: formattedContent }} />
        );
        return;
      }
      
      // If we were in a list and hit something else, flush it
      if (inList && !line.startsWith('- ') && !line.match(/^\d+\. /)) {
        flushList();
      }
      
      // Checkmarks
      if (line.startsWith('✅ ')) {
        elements.push(
          <div key={i} className="flex items-center gap-3 text-emerald-400 my-2">
            <span className="text-lg">✅</span>
            <span className="text-white/80">{line.replace('✅ ', '')}</span>
          </div>
        );
        return;
      }
      if (line.startsWith('❌ ')) {
        elements.push(
          <div key={i} className="flex items-center gap-3 text-red-400 my-2">
            <span className="text-lg">❌</span>
            <span className="text-white/80">{line.replace('❌ ', '')}</span>
          </div>
        );
        return;
      }
      
      // Blockquotes
      if (line.startsWith('> ')) {
        elements.push(
          <blockquote key={i} className="border-l-4 border-[hsl(268,83%,55%)] pl-6 py-3 my-6 bg-white/[0.02] rounded-r-lg">
            <p className="text-white/80 italic text-lg">{line.replace('> ', '')}</p>
          </blockquote>
        );
        return;
      }
      
      // Code blocks - skip markers
      if (line.startsWith('```')) {
        return;
      }
      
      // Empty lines
      if (line.trim().length === 0) {
        elements.push(<div key={i} className="h-4" />);
        return;
      }
      
      // Tables - render properly
      if (line.startsWith('|')) {
        // Skip separator lines
        if (line.includes('---')) return;
        
        const cells = line.split('|').filter(cell => cell.trim());
        const isHeader = lines[i + 1]?.includes('---');
        
        if (isHeader) {
          elements.push(
            <div key={i} className="overflow-x-auto my-6">
              <table className="w-full border-collapse">
                <thead>
                  <tr className="border-b border-white/20">
                    {cells.map((cell, j) => (
                      <th key={j} className="text-left py-3 px-4 text-white font-semibold text-sm">
                        {cell.trim()}
                      </th>
                    ))}
                  </tr>
                </thead>
              </table>
            </div>
          );
        } else if (!lines[i - 1]?.includes('---') && !lines[i - 1]?.startsWith('|')) {
          // Regular table row without header
          elements.push(
            <div key={i} className="border-b border-white/5">
              <div className="grid grid-cols-3 gap-4 py-3">
                {cells.map((cell, j) => (
                  <span key={j} className="text-white/70 text-sm">{cell.trim()}</span>
                ))}
              </div>
            </div>
          );
        }
        return;
      }
      
      // Bold text and regular paragraphs
      const boldFormatted = line.replace(/\*\*([^*]+)\*\*/g, '<strong class="text-white font-semibold">$1</strong>');
      elements.push(
        <p key={i} className="text-white/70 leading-relaxed mb-4 text-lg" dangerouslySetInnerHTML={{ __html: boldFormatted }} />
      );
    });
    
    // Flush any remaining list
    flushList();
    
    return elements;
  };

  return (
    <ServicePageLayout>
      <Helmet>
        <title>{article.title} — База знаний WBGen</title>
        <meta name="description" content={`${article.title}. Подробная инструкция в базе знаний WBGen.`} />
        <link rel="canonical" href={`https://wbgen.ru/baza-znaniy/article/${articleId}`} />
      </Helmet>

      <section className="pt-28 pb-16 sm:pt-36 sm:pb-24">
        <div className="container mx-auto px-4 sm:px-6">
          {/* Breadcrumbs */}
          <nav className="flex items-center gap-2 text-sm text-white/50 mb-8">
            <Link to="/baza-znaniy" className="hover:text-white transition-colors">База знаний</Link>
            <ChevronRight className="w-4 h-4" />
            <span className="text-white/70">{article.category}</span>
            <ChevronRight className="w-4 h-4" />
            <span className="text-white">{article.title}</span>
          </nav>

          {/* Back button */}
          <Link to="/baza-znaniy" className="inline-flex items-center gap-2 text-white/60 hover:text-white transition-colors mb-8">
            <ArrowLeft className="w-4 h-4" />
            Назад к базе знаний
          </Link>

          <div className="max-w-4xl">
            <motion.div
              initial={{ opacity: 0, y: 20 }}
              animate={{ opacity: 1, y: 0 }}
              transition={{ duration: 0.5 }}
            >
              {/* Article header card */}
              <div className="glass-card rounded-2xl p-8 sm:p-10 mb-8 border border-white/10">
                <span className="inline-block px-4 py-1.5 rounded-full bg-[hsl(268,83%,55%)]/20 text-[hsl(268,83%,65%)] text-sm font-medium mb-6">
                  {article.category}
                </span>
                <h1 className="text-3xl sm:text-4xl lg:text-5xl font-bold text-white mb-6 leading-tight">
                  {article.title}
                </h1>
                <div className="flex items-center gap-6 text-white/50 text-sm">
                  <div className="flex items-center gap-2">
                    <Clock className="w-4 h-4" />
                    <span>{article.readTime} чтения</span>
                  </div>
                  <div className="flex items-center gap-2">
                    <User className="w-4 h-4" />
                    <span>Команда WBGen</span>
                  </div>
                </div>
              </div>

              {/* Article content card */}
              <div className="glass-card rounded-2xl p-8 sm:p-10 border border-white/10">
                <div className="prose prose-invert prose-lg max-w-none">
                  {formatContent(article.content)}
                </div>
              </div>

              {/* CTA card */}
              <div className="mt-8 glass-card rounded-2xl p-8 sm:p-10 border border-[hsl(268,83%,55%)]/30 bg-gradient-to-br from-[hsl(268,83%,55%)]/10 to-[hsl(280,90%,55%)]/5">
                <h3 className="text-xl font-bold text-white mb-3">Готовы попробовать?</h3>
                <p className="text-white/60 mb-6">Зарегистрируйтесь и получите токены для тестирования</p>
                <Link to={withUtm("/auth?tab=signup")}>
                  <Button className="bg-gradient-to-r from-[hsl(268,83%,55%)] to-[hsl(280,90%,55%)] text-white border-0">
                    Начать
                  </Button>
                </Link>
              </div>
            </motion.div>
          </div>
        </div>
      </section>
    </ServicePageLayout>
  );
};

export default KnowledgeArticle;